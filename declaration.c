#include <stdio.h>

int main() {
    int myInt = 10;       // Integer variable
    float myFloat = 3.14; // Floating-point variable
    char myChar = 'A';    // Character variable

    // Printing the values
    printf("Integer: %d\n", myInt);
    printf("Float: %.2f\n", myFloat);
    printf("Character: %c\n", myChar);

    return 0;
}
